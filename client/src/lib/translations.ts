/**
 * Get translation for a word in the specified language
 * This function works completely offline with no API calls needed
 */
export async function getTranslation(word: string, languageCode: string): Promise<string> {
  // Normalize the word (lowercase for lookup)
  const normalizedWord = word.toLowerCase();
  
  // Get translation from our offline dataset
  if (translations[languageCode]?.[normalizedWord]) {
    return translations[languageCode][normalizedWord];
  }
  
  // If we don't have a translation, return the original word with proper capitalization
  return word.charAt(0).toUpperCase() + word.slice(1);
}

/**
 * Get pronunciation hint for a word in the specified language
 */
export function getPronunciation(word: string, languageCode: string): string | undefined {
  const normalizedWord = word.toLowerCase();
  return pronunciationHints[languageCode]?.[normalizedWord];
}
